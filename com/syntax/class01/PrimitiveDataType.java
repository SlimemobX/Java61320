package com.syntax.class01;

public class PrimitiveDataType {
	
	public static void main(String[] args) {
		
		/** Primitive data types
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char 
		 * boolean
		 */
		
		
		// Whole number data types
		byte num = 10;
				
		short num1 = 100;
		
		int num2 = 1000;
		
		// l or L
		long num3 = 1000000000986965L;
		
		
	
		// Decimal number data type
		
		// f or F
		float num4 = 12.9F;
		
		double num5 = 0.888;
		
		// Non-Number data type
		
		char add = '+';
		
		char letter = 'a';
		
		// True or False (yes or no)
		
		// don't put yes or no for value
		
		boolean hungry = true;
		
		boolean thirsty = false;	
		
		
	}
	
	

}
